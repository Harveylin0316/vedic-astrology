// ═══════════════════════════════════════════════════════════════
// 依年齡段挑選對應的 flavor
// ═══════════════════════════════════════════════════════════════
// 如果一段大運跨越兩個年齡段，以中點為準
export function getLifeFlavor(ageStart, ageEnd) {
  const mid = ((ageStart < 0 ? 0 : ageStart) + ageEnd) / 2
  if (mid < 13) return 'childFlavor'
  if (mid < 25) return 'teenFlavor'
  if (mid < 45) return 'primeFlavor'
  if (mid < 65) return 'matureFlavor'
  return 'elderFlavor'
}

