// 取得 Lagna-Moon 組合解讀
export function getLagnaMoonCombo(lagnaRashi, moonRashi) {
  const lagnaIdx = RASHI_ORDER.indexOf(lagnaRashi)
  const moonIdx = RASHI_ORDER.indexOf(moonRashi)
  if (lagnaIdx < 0 || moonIdx < 0) return null
  const distance = ((moonIdx - lagnaIdx + 12) % 12) + 1
  return {
    distance,
    ...DISTANCE_READINGS[distance]
  }
}
